package com.jaeyeonling.korean;

public class OnsetTokenizer extends KoreanTokenizer {

    // {'ㄱ', 'ㄲ', 'ㄴ', 'ㄷ', 'ㄸ', 'ㄹ', 'ㅁ', 'ㅂ', 'ㅃ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅉ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ'}
    private static final char[] ONSETS = {0x3131, 0x3132, 0x3134, 0x3137, 0x3138, 0x3139, 0x3141, 0x3142, 0x3143,
            0x3145, 0x3146, 0x3147, 0x3148, 0x3149, 0x314a, 0x314b, 0x314c, 0x314d, 0x314e};

    public OnsetTokenizer() {
        super(ONSETS);
    }

    @Override
    int getCriteria(final char character) {
        return ((character - (character % 28)) / 28) / 21;
    }
}
